  document.addEventListener("DOMContentLoaded", function () {
    let tabButton = document.querySelector("#nav-tab .navigation[data-bs-target]"); // Only the button
    let megaMenu = document.querySelector("#nav-tabContent");

    if (tabButton) {
        tabButton.addEventListener("mouseover", function () {
            let target = document.querySelector(tabButton.getAttribute("data-bs-target"));

            document.querySelectorAll("#nav-tabContent .tab-pane").forEach(pane => {
                pane.classList.remove("show", "active");
            });

            target.classList.add("show", "active");
            tabButton.classList.add("active");
        });
    }

    function closeMegaMenu() {
        setTimeout(() => {
            if (!tabButton.matches(":hover") && !megaMenu.matches(":hover")) {
                document.querySelectorAll("#nav-tabContent .tab-pane").forEach(pane => {
                    pane.classList.remove("show", "active");
                    tabButton.classList.remove("active");
                });
            }
        }, 100);
    }

    if (tabButton) {
        tabButton.addEventListener("mouseleave", closeMegaMenu);
    }

    megaMenu.addEventListener("mouseleave", closeMegaMenu);
});

let categoriesData = {};  // Declare a global variable

// Function to fetch categories and subcategories from the database
async function fetchCategories() {
  try {
    const response = await fetch('getCategoriesAndSubcategories');
    categoriesData = await response.json();
    populateMegaMenu(categoriesData);  // Update the mega menu after fetching data
  } catch (error) {
    console.error("Error fetching categories and subcategories:", error);
  }
}

// Function to populate the mega menu with categories and subcategories
function populateMegaMenu(categoriesData) {
  const firstSideContent = document.querySelector('.tab-navigation.one .mega-sides');
  const secondSideContent = document.getElementById('second-side-content');
  const thirdSideContent = document.getElementById('third-side-content');

  // Clear existing categories and subcategories
  firstSideContent.innerHTML = '';
  secondSideContent.innerHTML = '';
  thirdSideContent.innerHTML = '';

  // Dynamically populate categories
  Object.keys(categoriesData).forEach(categoryId => {
    const category = categoriesData[categoryId];
    const categoryElement = document.createElement('p');
    categoryElement.classList.add('category');
    categoryElement.setAttribute('data-category', categoryId);
    categoryElement.textContent = category.category_name;

    // Append to first mega side
    firstSideContent.appendChild(categoryElement);

    // Add event listener to category to update subcategories
    categoryElement.addEventListener('mouseover', () => updateSecondMegaSide(categoryId, categoriesData));
  });

  // Automatically select the first category to load its subcategories
  const firstCategoryId = Object.keys(categoriesData)[0];
  if (firstCategoryId) {
    updateSecondMegaSide(firstCategoryId, categoriesData);

    // Ensure the first category has the active class
    const firstCategoryElement = document.querySelector(`.category[data-category="${firstCategoryId}"]`);
    if (firstCategoryElement) {
      firstCategoryElement.classList.add('active');
    }
  }
}

// Function to update the content of the second mega side based on the selected category
function updateSecondMegaSide(categoryId, categoriesData) {
  const secondSideContent = document.getElementById('second-side-content');
  const category = categoriesData[categoryId];

  // Clear current content of the second mega side
  secondSideContent.innerHTML = '';

  // Create new content based on selected category
  const subcategories = category.subcategories.map(subCategory => 
    `<p class="sub-category" data-category="${subCategory.subcategory_id}">${subCategory.subcategory_name}</p>`
  ).join('');

  secondSideContent.innerHTML = subcategories;

  // Remove 'active' class from all sub-categories and set the first one as active
  const firstSubCategory = secondSideContent.querySelector('.sub-category');
  if (firstSubCategory) {
    firstSubCategory.classList.add('active');
    updateThirdMegaSide(firstSubCategory.getAttribute('data-category'));
  }

  // Add event listener to subcategories for third mega side update
  secondSideContent.addEventListener('mouseover', (event) => {
    if (event.target && event.target.classList.contains('sub-category')) {
      // Remove 'active' class from all sub-categories
      document.querySelectorAll('.sub-category').forEach(c => c.classList.remove('active'));

      // Add 'active' class to the clicked sub-category
      event.target.classList.add('active');

      // Update third mega side with sub-category content
      updateThirdMegaSide(event.target.getAttribute('data-category'));
    }
  });
}

async function updateThirdMegaSide(subcategoryId) {
    const thirdSideContent = document.getElementById('third-side-content');
    thirdSideContent.innerHTML = '<p>Loading ads...</p>';

    try {
        const response = await fetch(`getAdsBySubCategory?subcategory_id=${subcategoryId}`);
        const ads = await response.json();

        if (!Array.isArray(ads)) {
            thirdSideContent.innerHTML = `<p>Error: Unexpected response format</p>`;
            return;
        }

        if (ads.length === 0) {
            thirdSideContent.innerHTML = '<p>No ads available in this subcategory.</p>';
            return;
        }

        let adsHTML = ads.map(ad => {
            const imagesHTML = ad.images.map(img => `<img src="${img}" alt="Ad Image">`).join('');

            return `
                <div class="ad-item">
                    <a href="detailed_ad.php?id=${ad.product_id}"><div class="ad-images">${imagesHTML}</div></a>
                    <a href="detailed_ad.php?id=${ad.product_id}"><p>${ad.business_name}</p></a>
                </div>
            `;
        }).join('');

        thirdSideContent.innerHTML = adsHTML;
    } catch (error) {
        console.error("Error fetching ads:", error);
        thirdSideContent.innerHTML = '<p>Error loading ads. Try again later.</p>';
    }
}

// Ensure only one category is active at a time
document.addEventListener('DOMContentLoaded', () => {
  // Fetch categories and initialize the page
  fetchCategories();

  // Event delegation for category clicks
  document.querySelector('.tab-navigation.one').addEventListener('mouseover', (event) => {
    if (event.target.classList.contains('category')) {
      // Remove 'active' class from all categories
      document.querySelectorAll('.category').forEach(c => c.classList.remove('active'));

      // Add 'active' class to the clicked category
      event.target.classList.add('active');

      // Update the second mega side based on the selected category
      const selectedCategoryId = event.target.getAttribute('data-category');
      updateSecondMegaSide(selectedCategoryId, categoriesData);
    }
  });
});
